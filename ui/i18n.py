def current_lang() -> str:
    return _LANG


def set_lang(lang: str) -> None:
    global _LANG
    if lang in _STRINGS:
        _LANG = lang


def toggle_lang() -> str:
    set_lang("en" if _LANG == "zh" else "zh")
    return _LANG


def t(key: str, **kwargs) -> str:
    text = _STRINGS.get(_LANG, {}).get(key) or _STRINGS["en"].get(key, key)
    if kwargs:
        return text.format(**kwargs)
    return text


def excel_headers() -> List[str]:
    return [
        t("col_seq"),
        t("col_holder"),
        t("col_location"),
        t("col_right_cert"),
        t("col_orig_cert"),
        t("col_cert"),
    ]


def rename_preview_headers() -> List[str]:
    return [
        t("rename_col_old"),
        t("rename_col_new"),
        t("rename_col_reference"),
        t("rename_col_status"),
    ]


def accent_label(accent_key: str) -> str:
    return t(f"accent_{accent_key}")
